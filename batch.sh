#!/bin/bash
set -euo pipefail

# ============================================================
# 关键字列表（每行一个关键字）
# ============================================================
KEYWORDS=(
  "上中国际 上海 高中"
  "世外 上海 高中"
  "包玉刚 上海 高中"
  "平和 上海 高中"
  "领科 上海 高中"
  "德怀特 上海 高中"
  "复旦附中国际部 上海 高中"
  "惠立 上海 高中"
  "上交附中 上海 高中"
  "星河湾 上海 高中"
  "WLSA 上海 高中"
  "协和双语 上海 高中"
  "宏润博源 上海 高中"
  "万科双语 上海 高中"
  "民办德闳 上海 高中"
  "民办中芯 上海 高中"
  "燎原双语 上海 高中"
  "民办位育 上海 高中"
  "光华启迪 上海 高中"
  "启能东方外国语 上海 高中"
  "诺达双语 上海 高中"
  "惠灵顿 上海 高中"
)

# ============================================================
# 配置
# ============================================================
PROGRESS_FILE=".batch_progress"   # 记录已完成关键字的文件
TOTAL=${#KEYWORDS[@]}

# ============================================================
# 工具函数
# ============================================================
is_done() {
  # 判断关键字是否已完成
  [ -f "$PROGRESS_FILE" ] && grep -qxF "$1" "$PROGRESS_FILE"
}

mark_done() {
  echo "$1" >> "$PROGRESS_FILE"
}

# ============================================================
# 主循环
# ============================================================
echo "========================================================"
echo "共 $TOTAL 个关键字，进度文件: $PROGRESS_FILE"
echo "========================================================"

DONE_COUNT=0
SKIP_COUNT=0

for keyword in "${KEYWORDS[@]}"; do
  if is_done "$keyword"; then
    echo "[SKIP] ($((SKIP_COUNT + DONE_COUNT + 1))/$TOTAL) 已完成，跳过: $keyword"
    ((SKIP_COUNT++)) || true
    continue
  fi

  CURRENT=$((SKIP_COUNT + DONE_COUNT + 1))
  echo "------------------------------------------------------------"
  echo "[RUN ] ($CURRENT/$TOTAL) 执行关键字: $keyword"
  echo "------------------------------------------------------------"

  if uv run main.py --platform xhs --lt qrcode --type search --keywords "$keyword" --get_comment true; then
    mark_done "$keyword"
    ((DONE_COUNT++)) || true
    echo "[OK  ] 完成: $keyword"
  else
    EXIT_CODE=$?
    echo "[FAIL] 执行失败 (exit=$EXIT_CODE)，关键字: $keyword" >&2
    echo "程序终止。修复问题后重新运行脚本，将从此关键字继续。" >&2
    exit $EXIT_CODE
  fi
done

echo "========================================================"
echo "全部完成！本次新执行 $DONE_COUNT 个，跳过 $SKIP_COUNT 个。"
echo "========================================================"